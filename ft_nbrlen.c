#include "libft.h"

int	ft_nbrlen(int n)
{
	int		num;
	int		i;

	i = 0;
	num = n;
	if (num == 0)
		return (1);
	while (num)
	{
		num /= 10;
		i++;
	}
	return ((n < 0) ? i + 1 : i);
}
