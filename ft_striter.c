void    ft_striter(char *s, void (*f)(char *))
{
	int i;
	int len;

	if (s && f)
	{
		i = 0;
		len = ft_strlen(s);
		while (i < len && s[i])
		{
			f(&s[i]);
			i++;
		}
	}
}