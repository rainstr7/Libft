#include "libft.h"

int		ft_volarr(char const *s, char c)
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (s[i])
	{
		if (i == 0 && s[i] != c)
			len++;
		else if (s[i] != c && s[i - 1] == c)
			len++;
		i++;
	}
	return (len);
}
